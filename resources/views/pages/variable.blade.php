                    <p class="text">
                        Além disso, é essencial acompanhar os suportes e resistências, que são preços que a ação tende a parar de cair ou subir, respectivamente. Também preste atenção em indicadores técnicos, como Média Móvel e Índice de Força Relativa (IFR), para auxiliar na tomada de decisões.
                    </p>
                </div>
                <div class="topic" id="topic15">
                    <h2>Dicionário do investidor:</h2>
                    <p class="text">
                        <strong>Stop loss:</strong> é um mecanismo que vende as ações quando elas chegam a um nível de perda pré-determinado. Por exemplo, se as ações atingirem uma porcentagem de 10% de perda, o sistema as vende automaticamente, é interessante para caso uma empresa esteja em decadência.

                    </p>
                    <p class="text">
                        <strong>Stop gain:</strong> é um mecanismo que é o oposto ao stop loss, isto é, quando as ações chegam a uma porcentagem de lucro pré-determinado, o sistema as vende imediatamente. É uma ferramenta que auxilia muito os investidores que não consigam acompanhar o mercado 100% do tempo.
                    </p>
                    <p class="text">
                        <strong>Risco de ações do governo:</strong> apesar do risco, movimentações governamentais causam pouco impacto no mercado de ações quando comparadas a métodos de renda fixa, sendo somente por meio de tributação.
                    </p>
                    <p class="text">
                        <strong>Risco de liquidez:</strong>muitas vezes não é possível vender todas as ações de uma vez com o preço de tela, ou seja, o preço que aparece no livro de ofertas do homebroker, sendo necessário vender em lotes menores e em preços menores, esta diferença da venda real e da venda efetiva é o risco ou custo de liquidez.
                    </p>
                    <p class="text">
                        <strong>Livro de ofertas:</strong>principais movimentações de determinada ação no mercado (seu preço nas últimas horas).
                    </p>
                    <p class="text">
                        <strong>Homebroker:</strong>seção que age como intermediador do usuário e com os ativos (código da empresa, a cotação e oscilação dos rendimentos).
                    <p class="text">
                        <strong>Via mesa:</strong>o investidor delega a compra para um assessor financeiro, portanto o custo é maior.
                        <br>
                        - Não é recomendado.
                    <p class="text">
                        <strong>Grafista:</strong>curto prazo, analisa padrões de preço e de quantidade dos dados.
                        <br>
                        - O rendimento diário que se espera é 0.
                        <br>
                        - Alto custo e estresse.
                    </p>
                    <p class="text">
                        <strong>Fundamentalista:</strong>longo prazo, analisa modelo de negócio dos seus ativos.

                        <br>
                        - Para seguir esta corrente, veja notícias, análises, o foco da empresa e sua relação com investidores.
                        <br>
                        - Melhor para a maioria dos investidores.
                    </p>
                </div>
