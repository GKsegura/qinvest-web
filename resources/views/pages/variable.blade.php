                <div class="topic" id="topic1">
                    <h2>O que é renda variável?</h2>
                    <p class="text">
                        A renda variável se define como investimentos no qual o retorno e o preço de compra de ativos variam, isto é, não é definido por uma taxa fixa como a SELIC. Devido a esta variabilidade e incerteza dos preços a renda variável tem maior risco de perda, portanto, o formato de renda variável mais importante e no qual recomendamos que iniciantes comecem baseia-se no mercado de ações da bolsa.
                    </p>
                </div>
                <div class="topic" id="topic2">
                    <h2>O que são ações?</h2>
                    <p class="text">
                        Ações são partes do patrimônio dos sócios de uma empresa S/A (Sociedade Anônima) com capital investido na bolsa de valores, ou seja, comprar uma ação equivale a se tornar dono de um pequeno pedaço da empresa investida.
                    </p>
                </div>
                <div class="topic" id="topic3">
                    <h2>Ações são um bicho de sete cabeças?</h2>
                    <p class="text">
                        A maioria dos investidores tem medo de entrar no mercado de ações por conta dos riscos, no entanto, tem maneiras de reduzir esse risco e aumentar suas chances de rentabilidade sem ter que se preocupar em perder tudo, até porque o mercado de ações não é um cassino e sim um investimento.
                    </p>
                </div>
                <div class="topic" id="topic4">
                    <h2>Riscos</h2>
                    <p class="text">
                        - Diversificar as ações é a melhor maneira de evitar os riscos. A maioria dos problemas relacionados ao risco ocorre quando o investidor não diversifica os ativos, isto é, compra ações de apenas um setor do mercado ou de um segmento, como investir somente em empresas de petróleo - se houver variação no preço do petróleo você pode ter um prejuízo generalizado. Muitas vezes é inteligente mesclar seus investimentos em renda fixa e renda variável.
                        <br>
                        - Elimine a ganância e o medo. O investidor que não vende uma ação lucrativa por esperança de aumentar muito pode acabar perdendo o que podia ter ganho e o investidor que não investe por medo do risco também acaba por perder ótimos negócios.
                        <br>
                        - Defina metas ou requisitos. A melhor maneira de não cair na ganância é definir requisitos, que muitas vezes são pessoais. Um exemplo é sempre vender uma ação caso o preço da ação atinja 10% do preço de compra. Existem ferramentas como o stop loss ou stop gain que podem auxiliar o usuário neste quesito.
                        <br>
                        - Investir em carteiras conhecidas com bom rendimento ao longo dos anos. Recomendamos 5 na nossa seção de ações.
                        <br>
                        - A longo prazo é o melhor ativo para guardar seu capital. Dificilmente, a longo prazo, investir em renda fixa seria mais lucrativo do que investir em ações.
                        <br>
                        - Se informe. Busque compreender como é a relação com investidores e outros indicativos que mostraremos mais à frente. A melhor fonte de informação sobre seus investimentos é a nota de corretagem, ou seja, a nota gerada a partir da compra ou venda de um ativo. Ela demonstra o preço da ação, a taxa de corretagem e os impostos e o custo dos emolumentos da B3.
                    </p>
                </div>
                <div class="topic" id="topic5">
                    <h2>Bolsa de Valores:</h2>
                    <p class="text">
                        O Brasil possui apenas uma bolsa que é a B3. Ela é o órgão responsável pelo mercado de ações e por meio dela as transações de compra e venda são realizadas e as informações do mercado são distribuídas. É também responsável por administrar os riscos do mercado.
                    </p>
                </div>
                <div class="topic" id="topic6">
                    <h2>Corretora de Valores:</h2>
                    <p class="text">
                        Assim como uma corretora imobiliária, a corretora de valores intermedia o investidor com o mercado de ações. Investir no mesmo banco que utiliza para a conta corrente muitas vezes pode confundir o que é sua renda mensal e sua renda a ser investida, recomendamos separar os seus investimentos do seu banco principal para outra corretora. Para quem deseja começar a investir e muito provavelmente em quantias pequenas, é recomendável procurar uma corretora sem taxa de corretagem, de transferência e de custódia.
                    </p>
                </div>
                <div class="topic" id="topic7">
                    <h2>Passo a Passo:</h2>
                    <p class="text">
                        Para começar a investir é necessário criar uma conta em uma corretora, então procure uma que melhor se adeque aos seus interesses. Após criar uma conta em uma corretora, o investidor deve transferir dinheiro para a conta da corretora e realizar compra e venda de ações a partir de um homebroker ou via mesa. Analise os movimentos do mercado e aguarde o melhor momento para compra ou venda.
                    </p>
                </div>
                <div class="topic" id="topic8">
                    <h2>Possíveis Custos:</h2>
                    <p class="text">
                        Ao investir em ações, há alguns custos que podem ser obrigatórios ou evitáveis:
                        <br>
                        <strong>Preço da ação:</strong> Valor de compra de uma ação no mercado.
                        <br>
                        <strong>Taxa de corretagem:</strong> É fundamental analisar a taxa de corretagem, isto é, o valor para movimentar ações - compra ou venda. Este valor é fixo, mas se o valor investido for baixo, é possível que seja um custo inviável, portanto, se a quantidade que tem disponível para investir é importante buscar corretoras cuja taxa seja zero.
                        <br>
                        <strong>Taxa de custódia:</strong> Taxa para manutenção da conta na corretora - apesar da maioria das corretoras atuais isentarem o cliente dessa taxa, algumas podem cobrá-lo.
                        <br>
                        <strong>Emolumentos da B3:</strong> Taxas para operar na bolsa, que são quase insignificantes em relação aos custos (0,031605% para pessoas físicas e 0,024105% para fundos de investimento).
                        <br>
                        <strong>Imposto de Renda.</strong>
                        <br>
                        <strong>Taxa de transferência:</strong> A taxa de transferência pode ocorrer quando o investidor transfere dinheiro da corretora para a conta corrente (a maioria das corretoras também não cobra).
                        <br>
                        <strong>Taxa de aluguel:</strong> É uma forma de renda ou um possível custo que explicaremos na seção das formas de ganho.
                    </p>
                </div>
                <div class="topic" id="topic9">
                    <h2>Formas de ganho:</h2>
                    <p class="text">
                        <strong>Ganho de capital ou Apreciação do preço:</strong> diferença de compra e venda. Por exemplo, se você compra uma ação por 10 reais e vende por 15 reais após um certo período, o ganho de capital foi de 50%.
                    </p>
                    <p class="text">
                        <strong>Dividendo ou Parte dos Lucros:</strong>Como citado anteriormente, ações são uma parcela ou parte de uma empresa, e os acionistas são sócios dessa empresa. Se a empresa obtiver lucro, o acionista receberá uma parte desse lucro proporcional à porcentagem de posse das ações da empresa e ao tempo retido dessas ações, chamado de dividendo. No caso deste recebimento, não há cobrança de imposto de renda, ou ele será retido na fonte pela empresa.
                    </p>
                    <p class="text">
                        Comumente, ações nas quais o lucro dos acionistas se baseia nos dividendos são as mais conservadoras. Caso deseje lucrar com este método, é interessante descobrir o dividend yield (a quantidade paga de dividendo pelo preço da ação).
                    </p>
                    <p class="text">
                        <strong>Taxa de Aluguel:</strong>A taxa de aluguel pode ser uma opção de ganho ou custo. Ela ocorre quando um investidor com ações que deseje mantê-las paradas por um longo prazo as aluga a um investidor que deseja vendê-las devido a uma possível queda para recomprar mais barato e devolver. Aquele que pega emprestado paga uma taxa de aluguel ao que empresta.
                    </p>
                </div>
                <div class="topic" id="topic10">
                    <h2>Formas de investir:</h2>
                    <p class="text">
                        <strong>Day trade:</strong> é a modalidade na qual um investidor compra e vende ações no período de 24 horas. É voltado para perfis grafistas, demanda tempo e atenção constante ao mercado de minuto a minuto e necessita de certa experiência no ramo de ações. É pago 20% de imposto de renda sobre o lucro recebido. Não é recomendado para iniciantes.
                    </p>
                    <p class="text">
                        <strong>Swing trade:</strong> é a modalidade na qual o investidor compra e vende ações no período de 2 ou mais dias. É mais recomendado para iniciantes, é voltado ao perfil fundamentalista e demanda atenção diária aos gráficos e rendimento dos ativos. Há isenção de imposto de renda em caso de venda mensal de no máximo R$20,000. Caso o valor vendido de ativos supere esse valor, é pago 15% de imposto sobre o lucro.
                    </p>
                </div>
                <div class="topic" id="topic11">
                    <h2>Nomenclatura de ações:</h2>
                    <p class="text">
                        Para compreender o que significa VALE5 ou PETR4 F, devemos explicar os tipos de ações. O número 3 após a sigla da empresa (Petrobras é PETR em maiúsculo) significa ação ordinária, isto é, ações nas quais o detentor tem direito a voto na assembleia e nas decisões da empresa. O número 4 ou 5 indica ação preferencial, ou seja, um tipo de ação que tem preferência no ganho dos dividendos e isenção ou redução de voto nas decisões. O número 11 é a combinação de ações ordinárias e preferenciais para maior liquidez (capacidade de venda rápida), visto que essas ações são compradas em lotes. A sigla F representa que a ação está no mercado fracionário. Como dito anteriormente, as ações são normalmente compradas em lotes (geralmente de 100 em 100), mas é possível comprar partes menores (de 1 a 99) neste formato.
                    </p>
                </div>
                <div class="topic" id="topic12">
                    <h2>Tipos de empresas e relação com o lucro:</h2>
                    <p class="text">
                        <strong>Large Caps</strong> são empresas cujo valor de mercado é maior que 10 bilhões de dólares, como a Petrobras, a B3 e a Vale.
                    </p>
                    <p class="text">
                        <strong>Blue Chips</strong> são empresas estáveis e grandes como a Apple, Coca-Cola e Bradesco.
                    </p>
                    <p class="text">
                        <strong>Mid Caps</strong> são empresas intermediárias com valor de mercado acima de 2 bilhões de dólares como a Embraer, Natura e MRV. É recomendável, principalmente para iniciantes, investir no máximo até Mid Caps, pois quanto maior e mais consolidada a empresa, menor o risco e maior a liquidez. Para perfis muito agressivos, existem as small e micro caps que são empresas com potencial de crescimento, mas cujo risco pode ser nocivo para aqueles sem experiência. O tamanho de uma empresa ou o valor total de mercado é obtido pelo produto do preço das ações pela circulação das ações distribuídas no mercado.
                    </p>
                </div>
                <div class="topic" id="topic13">
                    <h2>Em caso de quebra da empresa ou da corretora:</h2>
                    <p class="text">
                        É quase impossível uma empresa quebrar sem nenhum indício prévio, e mesmo se isso ocorrer, nenhuma dívida da empresa é transferida para o acionista. No entanto, caso a corretora quebre, a B3 garante R$120,000,00 da conta corrente do investidor (se proveniente de ganhos na bolsa) e os ativos financeiros também são protegidos.
                    </p>
                </div>
                <div class="topic" id="topic14">
                    <h2>Análise de gráficos:</h2>
                    <p class="text">
                        O primeiro fator a se analisar no gráfico é a tendência em que ele está, ou seja, ascendente ou descendente. É importante observar as quebras de estrutura, que são momentos que a tendência se quebra. Ela normalmente não modifica a direção da tendência, mas pode ser um forte indicativo.
                    </p>
